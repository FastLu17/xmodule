package com.penglecode.xmodule.hydra.oauth2.examples.config;

public class HydraConfigProperties {

	private String publicApiUrl;
	
	private String adminApiUrl;

	public String getPublicApiUrl() {
		return publicApiUrl;
	}

	public void setPublicApiUrl(String publicApiUrl) {
		this.publicApiUrl = publicApiUrl;
	}

	public String getAdminApiUrl() {
		return adminApiUrl;
	}

	public void setAdminApiUrl(String adminApiUrl) {
		this.adminApiUrl = adminApiUrl;
	}
	
}
